package com.tdjpartner.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;


import com.tdjpartner.ui.fragment.DiscountCouponFragment;

import java.util.List;

public class FragmentDiscountCouponAdapter extends FragmentStatePagerAdapter {

    private List<String> mTitles;

    public FragmentDiscountCouponAdapter(FragmentManager fm, List<String> titles) {
        super(fm);
        mTitles = titles;
    }

    @Override
    public Fragment getItem(int position) {
        return DiscountCouponFragment.newInstance(position);
    }

    @Override
    public int getCount() {
        return mTitles.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mTitles.get(position);
    }
}